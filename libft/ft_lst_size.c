#include "libft.h"

int		ft_lst_size(t_list *start)
{
	size_t		i;
	t_list		*ptr;

	ptr = start;
	i = 0;
	while (ptr)
	{
		ptr = ptr->next;
		i++;
	}
	return (i);
}
