#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*a;

	i = 0;
	a = (unsigned char*)s;
	while (n != 0)
	{
		a[i] = '\0';
		i++;
		n--;
	}
	return (*s);
}
