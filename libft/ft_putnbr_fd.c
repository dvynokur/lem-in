#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int a;

	a = n;
	if (a == -2147483648)
		n = -214748364;
	if (a < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	if (n < 10)
		ft_putchar_fd(n + 48, fd);
	if (a == -2147483648)
		ft_putchar_fd('8', fd);
}
