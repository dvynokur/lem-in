#include "libft.h"

void	ft_putnbr(int n)
{
	int a;

	a = n;
	if (a == -2147483648)
		n = -214748364;
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	if (n < 10)
		ft_putchar(n + '0');
	if (a == -2147483648)
		ft_putchar('8');
}
