#include "libft.h"

void	ft_memdel(void **ap)
{
	size_t i;

	i = 0;
	if (ap)
	{
		while (ap[i])
			i++;
		while (ap[i])
		{
			free(ap[i]);
			i--;
		}
		*ap = NULL;
	}
}
